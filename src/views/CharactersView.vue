			& > div {
				position: relative;

				&:not(.owned) {
					filter: saturate(0.25);

					.constellation-overlay {
						display: none;
					}
				}

				.constellation-overlay {
					background-color: var(--button-background-color);
					pointer-events: none;
					z-index: 5;

					position: absolute;
					bottom: 1.3rem;
					height: 1em;
					width: 5rem;
					padding: 0.25em 0;

					color: var(--button-font-color);
					font-family: Hoyofont;
					font-size: 0.8rem;
					text-align: center;
				}
			}
		}
	}
</style>
